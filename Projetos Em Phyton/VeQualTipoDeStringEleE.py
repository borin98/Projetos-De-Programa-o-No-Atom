n = input ( "Digite algo : " )

print ( "Voce digitou um tipo : " , type ( n ) )

print ( "Isto possui espaços ? : ", n.isspace (  ) )

print ( "Isto é um número ? :  ", n.isnumeric (  ) )

print ( "Isto é um caracter ? : ", n.isalpha (  ) )

print ( "Isto é um Alphanum ? : ", n.isalnum ( ) )

print ( "Isto está apenas em maiúsculo ? : ", n.isupper (  ) )

print ( "Isto está apenas em minúsculo ? : ", n.islower (  ) )

print ( "Isto está capitalizada ( Maiúsculo e minúscula na mesma palavra ) ? : ", n.istitle (  ) )
